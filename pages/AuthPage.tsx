
import React, { useState, useEffect } from 'react';
import { supabase } from '../lib/supabase';
import { useNavigate } from 'react-router-dom';

type AuthMode = 'login' | 'signup' | 'forgot';

const AuthPage: React.FC = () => {
  const [email, setEmail] = useState('');
  const [password, setPassword] = useState('');
  const [showPassword, setShowPassword] = useState(false);
  const [mode, setMode] = useState<AuthMode>('login');
  const [loading, setLoading] = useState(false);
  const [error, setError] = useState('');
  const [message, setMessage] = useState('');
  const navigate = useNavigate();

  // حساب قوة كلمة المرور (0 to 4)
  const getPasswordStrength = (pwd: string) => {
    let strength = 0;
    if (pwd.length > 6) strength++;
    if (/[A-Z]/.test(pwd)) strength++;
    if (/[0-9]/.test(pwd)) strength++;
    if (/[^A-Za-z0-9]/.test(pwd)) strength++;
    return strength;
  };

  const handleAuth = async (e: React.FormEvent) => {
    e.preventDefault();
    setLoading(true);
    setError('');
    setMessage('');

    try {
      if (mode === 'login') {
        const { error } = await supabase.auth.signInWithPassword({ email, password });
        if (error) throw error;
        navigate('/game');
      } else if (mode === 'signup') {
        const { error: signUpError } = await supabase.auth.signUp({ 
          email, 
          password,
          options: {
            data: { is_admin: false, onboarding_completed: false }
          }
        });
        if (signUpError) throw signUpError;
        setMessage('ENLISTMENT SUCCESSFUL: Check your email for verify link.');
        setMode('login');
      } else {
        const { error: resetError } = await supabase.auth.resetPasswordForEmail(email, {
          redirectTo: window.location.origin + '/#/auth?type=recovery',
        });
        if (resetError) throw resetError;
        setMessage('RECOVERY LINK TRANSMITTED: Check your comm-link (email).');
      }
    } catch (err: any) {
      setError(err.message || 'CRITICAL AUTHENTICATION FAILURE');
    } finally {
      setLoading(false);
    }
  };

  const handleGoogleLogin = async () => {
    try {
      const { error } = await supabase.auth.signInWithOAuth({
        provider: 'google',
        options: { redirectTo: window.location.origin }
      });
      if (error) throw error;
    } catch (err: any) {
      setError(err.message);
    }
  };

  const strength = getPasswordStrength(password);

  return (
    <div className="min-h-screen bg-[#05080a] flex items-center justify-center p-6 relative overflow-hidden font-sans">
      {/* Background Tech Grid */}
      <div className="absolute inset-0 z-0 bg-[linear-gradient(to_right,#80808012_1px,transparent_1px),linear-gradient(to_bottom,#80808012_1px,transparent_1px)] bg-[size:40px_40px]"></div>
      <div className="absolute top-1/2 left-1/2 -translate-x-1/2 -translate-y-1/2 w-[600px] h-[600px] bg-emerald-500/5 blur-[120px] rounded-full"></div>

      <div className="w-full max-w-md relative z-10">
        {/* Top Branding */}
        <div className="text-center mb-10 group">
          <h2 className="text-sm font-black text-emerald-500 tracking-[0.6em] uppercase mb-2 opacity-80 group-hover:opacity-100 transition-opacity">
            System Protocol
          </h2>
          <h1 className="text-4xl font-gaming text-white italic uppercase tracking-tighter">
            {mode === 'login' ? 'Authorization' : mode === 'signup' ? 'Enlistment' : 'Recovery'}
          </h1>
        </div>

        <div className="bg-slate-900/40 backdrop-blur-xl border border-white/5 p-8 md:p-10 shadow-2xl relative overflow-hidden">
          {/* Top accent line */}
          <div className="absolute top-0 left-0 w-full h-[2px] bg-gradient-to-r from-transparent via-emerald-500 to-transparent"></div>

          {error && (
            <div className="mb-6 p-4 bg-red-500/10 border-l-2 border-red-500 text-red-400 text-[10px] font-bold uppercase tracking-widest animate-pulse">
              [SYSTEM ERROR]: {error}
            </div>
          )}

          {message && (
            <div className="mb-6 p-4 bg-emerald-500/10 border-l-2 border-emerald-500 text-emerald-400 text-[10px] font-bold uppercase tracking-widest">
              [MESSAGE]: {message}
            </div>
          )}

          <form onSubmit={handleAuth} className="space-y-6">
            <div className="space-y-1">
              <label className="text-[10px] text-slate-500 uppercase font-black tracking-[0.2em] ml-1">Email Identifier</label>
              <input 
                type="email" 
                required
                className="w-full bg-black/50 border border-slate-800 p-4 text-white focus:border-emerald-500 outline-none transition-all placeholder:text-slate-800 font-mono text-sm"
                placeholder="operator@caliquest.io"
                value={email}
                onChange={(e) => setEmail(e.target.value)}
              />
            </div>

            {mode !== 'forgot' && (
              <div className="space-y-1">
                <div className="flex justify-between items-end">
                  <label className="text-[10px] text-slate-500 uppercase font-black tracking-[0.2em] ml-1">Access Cipher</label>
                  {mode === 'login' && (
                    <button 
                      type="button" 
                      onClick={() => setMode('forgot')}
                      className="text-[9px] text-emerald-500/50 hover:text-emerald-400 uppercase font-bold tracking-widest mb-1 transition-colors"
                    >
                      Forgot Cipher?
                    </button>
                  )}
                </div>
                <div className="relative">
                  <input 
                    type={showPassword ? "text" : "password"} 
                    required
                    className="w-full bg-black/50 border border-slate-800 p-4 text-white focus:border-emerald-500 outline-none transition-all placeholder:text-slate-800 font-mono text-sm"
                    placeholder="••••••••"
                    value={password}
                    onChange={(e) => setPassword(e.target.value)}
                  />
                  <button 
                    type="button"
                    onClick={() => setShowPassword(!showPassword)}
                    className="absolute right-4 top-1/2 -translate-y-1/2 text-slate-700 hover:text-emerald-500 transition-colors"
                  >
                    {showPassword ? 'HIDE' : 'SHOW'}
                  </button>
                </div>
                
                {/* Strength Meter for Signup */}
                {mode === 'signup' && password.length > 0 && (
                  <div className="mt-2 flex gap-1 h-1">
                    {[1, 2, 3, 4].map((i) => (
                      <div 
                        key={i} 
                        className={`flex-1 transition-all duration-500 ${i <= strength ? (strength <= 2 ? 'bg-yellow-500' : 'bg-emerald-500') : 'bg-slate-900'}`}
                      />
                    ))}
                  </div>
                )}
              </div>
            )}

            <button 
              type="submit" 
              disabled={loading}
              className="w-full py-5 bg-white text-black font-black uppercase tracking-[0.3em] text-xs hover:bg-emerald-400 transition-all disabled:opacity-50 disabled:cursor-not-allowed relative group overflow-hidden"
            >
              <span className="relative z-10">{loading ? 'Processing...' : mode === 'login' ? 'Infiltrate' : mode === 'signup' ? 'Create Identity' : 'Transmit Link'}</span>
              <div className="absolute inset-0 bg-emerald-500 translate-y-full group-hover:translate-y-0 transition-transform duration-300"></div>
            </button>
          </form>

          <div className="relative flex items-center py-8">
            <div className="flex-grow border-t border-slate-800"></div>
            <span className="flex-shrink mx-4 text-slate-700 text-[9px] font-black uppercase tracking-[0.4em]">External Sync</span>
            <div className="flex-grow border-t border-slate-800"></div>
          </div>

          <button 
            onClick={handleGoogleLogin}
            className="w-full py-4 border border-slate-800 hover:border-emerald-500/50 text-slate-400 hover:text-white transition-all flex items-center justify-center gap-3 text-[10px] font-bold uppercase tracking-widest"
          >
            <svg className="w-4 h-4" viewBox="0 0 24 24">
              <path fill="currentColor" d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z" />
              <path fill="currentColor" d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z" />
              <path fill="currentColor" d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z" />
              <path fill="currentColor" d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z" />
            </svg>
            Google Neural Auth
          </button>

          <div className="mt-8 text-center space-y-2">
            {mode === 'login' ? (
              <button onClick={() => setMode('signup')} className="text-[10px] text-slate-500 hover:text-emerald-500 font-bold uppercase tracking-widest transition-colors">
                New User? <span className="text-white">Request Access</span>
              </button>
            ) : (
              <button onClick={() => setMode('login')} className="text-[10px] text-slate-500 hover:text-emerald-500 font-bold uppercase tracking-widest transition-colors">
                Known Operator? <span className="text-white">Return to Terminal</span>
              </button>
            )}
          </div>
        </div>
        
        {/* Visual Footer for Auth */}
        <div className="mt-8 flex justify-between items-center px-2 opacity-20">
          <div className="text-[8px] text-slate-500 font-mono">NODE_LOCATION: {window.location.hostname}</div>
          <div className="text-[8px] text-slate-500 font-mono">ENCRYPTION: AES-256</div>
        </div>
      </div>
    </div>
  );
};

export default AuthPage;
